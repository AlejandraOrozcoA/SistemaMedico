    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            String pa = jTextField1.getText();
            String ef = jTextField2.getText();
            String dx = jTextField3.getText();
            String tx = jTextField4.getText();
            String estudios = jTextField5.getText();
            
            String fecha_str = jTextField6.getText();
            Date fecha;
            try{
                fecha = UtilsEntradas.getFechaDeString(fecha_str);
            } catch(ParseException e){                
                throw new Exception("La fecha debe estar en formato dd/mm/aaa");
            }
            
            AltasBD alta = new AltasBD();
            alta.registrarConsultaSubsecuente(
                this.historia.getIDHistoria(),
                pa,
                ef,
                dx,
                tx,
                estudios,
                fecha
            );
            
            JOptionPane.showMessageDialog(
                null, "Consulta subsecuente agregada");
            
            this.setVisible(false);
            this.framePadre.setVisible(true);
            
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());               
        }
    }//GEN-LAST:event_jButton2ActionPerformed

